;16 BIT SUBTRACTION USING REGISTER INDIRECT MODE
;use  'TD FILE_NAME' to check the output

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
  NUM1 DW 30H
  NUM2 DW 10H
  RES DW ?
DATA ENDS

CODE SEGMENT
  START:
    MOV AX,DATA
    MOV DS,AX

    LEA SI,NUM1
    MOV AX,[SI]
    LEA SI,NUM2
    MOV DX,[SI]
    SUB AX,DX
    LEA SI,RES
    MOV [SI],AX
   
    MOV AH,4CH
    INT 21H

CODE ENDS
END START
